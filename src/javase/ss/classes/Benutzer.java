package javase.ss.classes;

/**
 *
 * @author CC-Student
 */
public class Benutzer {
    
    private int penutzerId;
    private String name;
    private String passwort;
    private String vollName;
    private String mail;
    private String phone;
    
    

    public int getPenutzerId() {
        return penutzerId;
    }

    public void setPenutzerId(int penutzerId) {
        this.penutzerId = penutzerId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPasswort() {
        return passwort;
    }

    public void setPasswort(String passwort) {
        this.passwort = passwort;
    }

    public String getVollName() {
        return vollName;
    }

    public void setVollName(String vollName) {
        this.vollName = vollName;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    
    
    
}
